for i in range (1, 11): # here i is a variable that takes the value of each number in the range from 1 to 10 on each iteration of the loop.
    print(i)

fruits = ["apple", "banana", "cherry"]

for x in fruits: # here x is a variable that takes the value of the item inside the fruits list on each iteration of the loop.
    print(x)

for y in range (20, 1, -1): # here y is a variable that takes the value of each number in the range from 20 to 2 on each iteration of the loop. and -1 indicates that the loop will decrement by 1 on each iteration.
    print(y)

for z in range (1, 1, 1):
    print(z)